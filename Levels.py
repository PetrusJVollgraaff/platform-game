from Objects import Ground

class Map:
    def __init__(self):
        self.map = []

    def draw(self, win):
        for i, row in enumerate(self.map):
            for j, col in enumerate(row):
                if col != 0:
                    col.draw(win)

    def add_Ground(self, floor_size):
        for i, row in enumerate(self.map):
            for j, col in enumerate(row):
                if col == 1:
                    self.map[i][j] = Ground((i * floor_size), (j * floor_size), floor_size)

        return self.map


